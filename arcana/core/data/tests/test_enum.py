from arcana.data.spaces.medicalimaging import ClinicalTrial

def test_is_parent():
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.session)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.timepoint)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.batch)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.session.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.member.is_parent(ClinicalTrial.session)
    assert ClinicalTrial.member.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.member.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.member.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.member.is_parent(ClinicalTrial.timepoint)
    assert not ClinicalTrial.member.is_parent(ClinicalTrial.batch)
    assert ClinicalTrial.member.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.member.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.group.is_parent(ClinicalTrial.session)
    assert ClinicalTrial.group.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.group.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.group.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.group.is_parent(ClinicalTrial.timepoint)
    assert ClinicalTrial.group.is_parent(ClinicalTrial.batch)
    assert not ClinicalTrial.group.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.group.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.timepoint.is_parent(ClinicalTrial.session)
    assert not ClinicalTrial.timepoint.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.timepoint.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.timepoint.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.timepoint.is_parent(ClinicalTrial.timepoint)
    assert ClinicalTrial.timepoint.is_parent(ClinicalTrial.batch)
    assert ClinicalTrial.timepoint.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.timepoint.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.subject.is_parent(ClinicalTrial.session)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.timepoint)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.batch)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.subject.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.batch.is_parent(ClinicalTrial.session)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.timepoint)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.batch)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.batch.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.session)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.subject)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.member)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.group)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.timepoint)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.batch)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.matchedpoint.is_parent(ClinicalTrial.dataset)

    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.session)
    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.subject)
    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.member)
    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.group)
    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.timepoint)
    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.batch)
    assert ClinicalTrial.dataset.is_parent(ClinicalTrial.matchedpoint)
    assert not ClinicalTrial.dataset.is_parent(ClinicalTrial.dataset)




